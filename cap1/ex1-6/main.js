// Lendo 2 números
const num1 = Number(prompt("Digite o 1º número:"));
const num2 = Number(prompt("Digite o 2º número:"));

// Calculando a soma e a subtração dos números lidos
const soma = num1 + num2;
const subtracao = num1 - num2;

// Mostrando os cálculos
alert(`A soma dos números digitados é = ${soma}`);
alert(`A subtração dos números digitados é = ${subtracao}`);
